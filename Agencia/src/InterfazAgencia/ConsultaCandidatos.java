    private void jBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBorrarActionPerformed
        // TODO add your handling code here:        
     if(Agencia.borraCandidato(objcand)){
        li.remove();
        JOptionPane.showMessageDialog(this, "Candidato borrado", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
    } else {
        JOptionPane.showMessageDialog(this, " Error: - Candidato no encontrado - ", "Mensaje", JOptionPane.ERROR_MESSAGE);
    }
    //mostramos el elemento siguiente o anterior
    if (li.hasNext()) {
        objcand = (Candidato) li.next();
        if (objcand != null) {
            presenta(objcand);
        } else {
            JOptionPane.showMessageDialog(this, "No hay candidatos.", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
        }
    } else if (li.hasPrevious()) {
        objcand = (Candidato) li.previous();
        if (objcand != null) {
            presenta(objcand);
        } else {
            JOptionPane.showMessageDialog(this, "No hay candidatos.", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
        }
    }else {
        JOptionPane.showMessageDialog(this, "No hay candidatos.", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
        this.formWindowClosed(null);
        this.setVisible(!true);
    }
        Agencia.guardarCandidatos();
    }//GEN-LAST:event_jBorrarActionPerformed

