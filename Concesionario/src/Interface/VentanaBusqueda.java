    private void consultarbusqueda() {
        try {
            //creamos el iterador sobre el ArrayList
            li = veh.listIterator();
            //si no hay vehiculos...
            if (veh.size() < 1) {
                JOptionPane.showMessageDialog(this, "No hay vehiculos.", "Mensaje", JOptionPane.INFORMATION_MESSAGE);   //mesaje de que no hay vehiculos
                jButton3.setEnabled(false);             //se desactivan los botones
                jButton4.setEnabled(false);
                jButton1.setEnabled(false);
                return;
            } else {                                    //si hubiera vehiculos
                jButton2.setEnabled(true);              //se activan los botones
                jButton1.setEnabled(true);
            }
            //presentamos los vehiculos
            if (li.hasNext()) {
                vehicul = (Vehiculo) li.next();         //se obtiene el vehiculo
            }
            if (vehicul != null) {                      //si no es nulo
                presentar(vehicul);                     //se muestra pasando el vehiculo
            } else {                                    //si es nulo no hay mas vehiculos
                JOptionPane.showMessageDialog(this, "No hay mas vehiculos.", "Mensaje", JOptionPane.INFORMATION_MESSAGE);   //mesnaje de no hay mas
                return;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Mensaje", JOptionPane.ERROR_MESSAGE);      //mensaje de error
            System.out.println("Error: " + e.toString());
            return;
        }
    }

