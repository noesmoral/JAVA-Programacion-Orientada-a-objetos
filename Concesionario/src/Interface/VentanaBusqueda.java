    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here
        //boton de comprar
        if (jTextField1.getText() != "") {                                      //si el campo no esta vacio se continua
            String ma = jTextField1.getText();                                  //se obtiene la matricula del vehiculo
            vehiculaux = almac.obtenervehiculo(ma);                             //con l amtricula se obtiene el vehiculo

            JPanel panel = new JPanel();                                        //se crea un nuevo panel
            JLabel label = new JLabel("Inserte Datos de administrador:");       //mensaje del panel
            JTextField user = new JTextField(10);                               //cuadro de texto del panel
            JPasswordField pass = new JPasswordField(4);                        //cuadro de contraseña del panel
            panel.add(label);                                                   //se añaden al panel
            panel.add(user);
            panel.add(pass);
            String[] options = new String[]{"OK", "Cancel"};                    //opciones del panel
            int option = JOptionPane.showOptionDialog(null, panel, "The title", JOptionPane.NO_OPTION, JOptionPane.PLAIN_MESSAGE, null, options, options[1]);   //se muestra y se recoje el resultado
            if (option == 0) //si presionado OK 
            {
                String password = String.valueOf(pass.getPassword());           //se obtiene la contraseña
                String usuario = user.getText();                                //se obtiene el usurname
                if ("admin".equals(usuario)) {                                  //si el username es igual a admin
                    if ("1111".equals(password)) {                              //si el pw es igual a 1111
                        String qw = "Empleado";                                 //valores aux
                        String wq = "Familiar";
                        if (cli.getrelacionConcesionario().equalsIgnoreCase(qw)) {              //si el cliente es empleado
                            fina = (vehiculaux.getprecio() - (vehiculaux.getprecio() * 0.25));  //se asigna precion final de empleado
                        } else if (cli.getrelacionConcesionario().equalsIgnoreCase(wq)) {       //si el cliente es familiar 
                            fina = (vehiculaux.getprecio() - (vehiculaux.getprecio() * 0.10));  //se asina precio final familiar
                        } else {
                            fina = vehiculaux.getprecio();
                        }
                        //se crea el objeto venta con toda la informacion de usuario y del vehiculo
                        vendido = new Venta(cli.getnombreApellido(), cli.getcodigoUsuario(), cli.getdni(), cli.getrelacionConcesionario(), vehiculaux.getmatricula(), vehiculaux.getmarca(), vehiculaux.getmodelo(), vehiculaux.getcolor(), Integer.toString(vehiculaux.getanofabricacion()), Integer.toString(vehiculaux.getkilometros()), fina, vehiculaux.tipoVehiculo(), Double.toString(vehiculaux.getpotencia()));
                        try {
                            ventad.insertarventa(vendido);                      //se mete en registroventas e imprimir factura
                        } catch (IOException ex) {
                            Logger.getLogger(VentanaBusqueda.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        almac.bajaVehiculo(vehiculaux.getmatricula());          //eliminar el vehiculo del almacen
                        JOptionPane.showMessageDialog(this, "Vehiculo Vendido", "Mensaje", JOptionPane.INFORMATION_MESSAGE);    //mostrar mensaje de vendido                        
                        this.setVisible(false);                                                                                 //se cierra la ventana 
                        this.dispose();                                                                                         //salir                      
                    } else {
                        JOptionPane.showMessageDialog(this, "No tienes permisos de administrador", "Error", JOptionPane.INFORMATION_MESSAGE);   //mensaje por si no se a insertado correctamente los datos de admin
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "No tienes permisos de administrador", "Error", JOptionPane.INFORMATION_MESSAGE);
                }

            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

