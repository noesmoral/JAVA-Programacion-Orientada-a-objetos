    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        //boton registrar
        if (jTextField1.getText().equals("") || jTextField2.getText().equals("") || String.valueOf(jPasswordField1.getPassword()).equals("") || jTextField4.getText().equals("")) {     //compra que no haya ningun campo vacio si es asi muestra mensaje de error
            JOptionPane.showMessageDialog(this, "No se han rellenado todos los campos o estan mal escritos", "Mensaje", JOptionPane.INFORMATION_MESSAGE);                               //mensaje de que falta por rellenar algo
        } else {            
            String arrr = String.valueOf(jPasswordField1.getPassword());    //obtiene la longitud de la cadena del pw
            if (arrr.length() == 4) {                                       //en caso de que la cadena sea de tama√±o 4 continua
                //crea un cliente con lo obtenido de todos los capos  
                Cliente a = new Cliente(jTextField1.getText(), jTextField2.getText(), jComboBox1.getSelectedItem().toString(), jTextField4.getText(), Integer.valueOf(String.valueOf(jPasswordField1.getPassword())));
                boolean res = persona.altausuario(a);                       //le da de alta en el registro personas y recibe la respuesta
                if (res == false) {                                         //si la respuesta del registro es false no se a insertado
                    jTextField4.setText("");                                //se vacia el username
                    JOptionPane.showMessageDialog(this, "Usuario en uso", "Error", JOptionPane.INFORMATION_MESSAGE);        //mesnaje d que no se a creado el regisrto por estar en uso
                } else {
                    //si es true se continua
                    JOptionPane.showMessageDialog(this, "Usuario Registrado", "Mensaje", JOptionPane.INFORMATION_MESSAGE);  //mesnaje de registro ok
                    jTextField1.setText("");                                //se limpian los campos
                    jTextField2.setText("");
                    jTextField4.setText("");
                    jPasswordField1.setText("");
                }
            } else {                                                        //si no cumple los 4 caracteres el pw
                jPasswordField1.setText("");                                //se limpia el campo pw
                JOptionPane.showMessageDialog(this, "La contrase√±a tiene que tener tama√±o 4", "Mensaje", JOptionPane.INFORMATION_MESSAGE);  //mesaje error pw
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

