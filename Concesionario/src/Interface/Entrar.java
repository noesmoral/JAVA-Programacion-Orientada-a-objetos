    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        //boton entrar
        if (jTextField1.getText().equals("") || String.valueOf(jPasswordField2.getPassword()).equals("")) {     //si no se a completado los campos error
            JOptionPane.showMessageDialog(this, "No se han rellenado todos los campos o estan mal escritos", "Mensaje", JOptionPane.INFORMATION_MESSAGE);   //mesnaje de que faltan campos por rellenar
        } else {
            String usuario = jTextField1.getText();                                         //obtiene el texto del campo username
            int pw = Integer.valueOf(String.valueOf(jPasswordField2.getPassword()));        //obtiene el texto del campo password
            if ("admin".equals(usuario)) {                                                  //en caso de que sea igual a admin continua si no salta al else
                if (pw == 1111) {                                                           //si ademas el pw es igual a 1111 entra como admin
                    jTextField1.setText("");                                                //limpia los campos
                    jPasswordField2.setText("");
                    VentanaAdmin s = new VentanaAdmin(this, almacen, venta);                //crea la ventana de entrada como admin y se pasa esta ventana el registro almacen y venta
                }else {
                    JOptionPane.showMessageDialog(this, "Usuario o Pasword Incorrecta", "Error", JOptionPane.INFORMATION_MESSAGE);
                }
            } else {                                                                        //no se a compludo que el user name es igual a admin   
                if (persona.contiene(usuario)) {                                            //si el user name esta en el registro de personas continua
                    if (persona.pw(usuario) == pw) {                                        //si esta la persona y el pw coincide con el suyo continua
                        Cliente us = persona.obtenerpersona(usuario);                       //se obtiene el cliente en cuestion
                        jTextField1.setText("");                                            //se limpian los campos
                        jPasswordField2.setText("");
                        VentanaUsuario s = new VentanaUsuario(this, almacen, venta, us);    //se crea la entrada como usuario se pasa la ventana el registro almacen venta y la persona que a entrado
                    } else {
                        JOptionPane.showMessageDialog(this, "Usuario o Pasword Incorrecta", "Error", JOptionPane.INFORMATION_MESSAGE);      //mensajes de error
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Usuario o Pasword Incorrecta", "Error", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

